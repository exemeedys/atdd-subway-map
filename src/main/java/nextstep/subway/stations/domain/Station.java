package nextstep.subway.stations.domain;

import javax.persistence.*;
import java.util.Objects;

@Entity
@Table(name = "station", uniqueConstraints = {
    @UniqueConstraint(name = "station_name_unique", columnNames = {"name"})
})
public class Station {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;

    public Station() {
    }

    public Station(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public boolean isSame(Station downStation) {
        return this.equals(downStation);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Station station = (Station) o;
        return Objects.equals(id, station.id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }
}
